package com.spadea.xqipao.utils.view.room;

public class RoomHeadInfo {

    private String roomName;
    private String roomTypeName;
    private String roomId;
    private String roomPopularity;
    private String isPassword;


    public String getIsPassword() {
        return isPassword;
    }

    public void setIsPassword(String isPassword) {
        this.isPassword = isPassword;
    }

    public String getRoomName() {
        return roomName;
    }

    public void setRoomName(String roomName) {
        this.roomName = roomName;
    }

    public String getRoomTypeName() {
        return roomTypeName;
    }

    public void setRoomTypeName(String roomTypeName) {
        this.roomTypeName = roomTypeName;
    }

    public String getRoomId() {
        return roomId;
    }

    public void setRoomId(String roomId) {
        this.roomId = roomId;
    }

    public String getRoomPopularity() {
        return roomPopularity;
    }

    public void setRoomPopularity(String roomPopularity) {
        this.roomPopularity = roomPopularity;
    }

}
