package com.spadea.xqipao.data;

public class GiftNumBean {


    private String num;
    private String name;

    public GiftNumBean(String num, String name) {
        this.num = num;
        this.name = name;
    }

    public String getNum() {
        return num;
    }

    public void setNum(String num) {
        this.num = num;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
