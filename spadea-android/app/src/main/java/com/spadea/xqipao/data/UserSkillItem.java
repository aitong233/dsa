public class UserSkillItem {
    private int skillId;
    private String applyPicture;
    private String voiceExample;
    private String skillName;
    private String price;
    private int number;
    private String company;
    private int skillConfigId;
    private int groupId;
    private int id;

    public int getSkillId() {
        return skillId;
    }

    public void setSkillId(int skillId) {
        this.skillId = skillId;
    }

    public String getApplyPicture() {
        return applyPicture;
    }

    public void setApplyPicture(String applyPicture) {
        this.applyPicture = applyPicture;
    }

    public String getVoiceExample() {
        return voiceExample;
    }

    public void setVoiceExample(String voiceExample) {
        this.voiceExample = voiceExample;
    }

    public String getSkillName() {
        return skillName;
    }

    public void setSkillName(String skillName) {
        this.skillName = skillName;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public int getNumber() {
        return number;
    }

    public void setNumber(int number) {
        this.number = number;
    }

    public String getCompany() {
        return company;
    }

    public void setCompany(String company) {
        this.company = company;
    }

    public int getSkillConfigId() {
        return skillConfigId;
    }

    public void setSkillConfigId(int skillConfigId) {
        this.skillConfigId = skillConfigId;
    }

    public int getGroupId() {
        return groupId;
    }

    public void setGroupId(int groupId) {
        this.groupId = groupId;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}
