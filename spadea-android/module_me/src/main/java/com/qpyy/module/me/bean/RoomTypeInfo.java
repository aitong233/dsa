package com.qpyy.module.me.bean;

public class RoomTypeInfo {


    private String text;
    private int type;

    public RoomTypeInfo(String text, int type) {
        this.text = text;
        this.type = type;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }
}
